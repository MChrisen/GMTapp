const baseNotes = ['Tegn altid figur, vælg koordinatsystem og skriv fortegn før du indsætter tal.'];

type FormulaInput = {
  id: string;
  name: string;
  category: FormulaCategory;
  topic: string;
  equation: string;
  latex: string;
  variables: Variable[];
  assumptions: string[];
  keywords: string[];
  sources: SourceRef[];
  calculatorId?: string;
  notes?: string[];
  relatedFormulaIds?: string[];
};

const formula = (input: FormulaInput): Formula => ({
  id: input.id,
  name: input.name,
  category: input.category,
  topic: input.topic,
  equation: input.equation,
  latex: input.latex,
  ...formulaText(input),
  variables: input.variables,
  assumptions: input.assumptions,
  notes: input.notes ?? baseNotes,
  keywords: input.keywords,
  sources: input.sources,
  calculatorId: input.calculatorId,
  relatedExampleIds: [],
  relatedFormulaIds: input.relatedFormulaIds ?? [],
});

const categoryUseText: Record<FormulaCategory, string> = {
  Kinematik: 'Brug den når opgaven handler om position, hastighed, acceleration eller tid.',
  Krafter: 'Brug den når opgaven kræver et kraftdiagram eller en resulterende kraft.',
  'Arbejde og energi': 'Brug den når opgaven kan løses ved energi, arbejde, effekt eller højde/fart før og efter.',
  Impuls: 'Brug den ved stød, kollisioner, rekyl, massecentrum eller bevægelsesmængde før og efter.',
  Rotation: 'Brug den når et legeme roterer, ruller, har moment, vinkelacceleration eller inertimoment.',
  Statik: 'Brug den når legemet er i hvile, i ligevægt eller på grænsen til at tippe.',
  Gravitation: 'Brug den ved planeter, satellitter, baner og afstande målt fra centrum.',
  Termodynamik: 'Brug den ved varme, temperatur, gas, pV-diagrammer, faseovergange eller maskiner.',
  'Konstanter og enheder': 'Brug den som en hurtig enheds- eller konstantomregning før du regner videre.',
};

const formulaText = (input: FormulaInput) => {
  const variableNames = input.variables
    .map((variable) => `${variable.symbol} (${variable.name})`)
    .slice(0, 4)
    .join(', ');
  const extraVariables = input.variables.length > 4 ? ' m.fl.' : '';
  const firstAssumption = input.assumptions[0] ?? 'Kontrollér altid at forudsætningerne matcher opgaven.';
  return {
    description: `${input.name} hører til ${input.topic.toLowerCase()} og forbinder ${variableNames}${extraVariables}. Den bruges typisk som et hurtigt startpunkt, når nøgleordene i opgaven matcher ${input.keywords.slice(0, 4).join(', ')}.`,
    useWhen: categoryUseText[input.category],
    examTip: `${firstAssumption} Isolér den ukendte symbolsk først og indsæt tal med enheder til sidst.`,
  };
};


import type { Formula, FormulaCategory, SourceRef, Variable } from './types';

const v = (symbol: string, name: string, unit?: string, latex?: string): Variable => ({ symbol, name, unit, latex });
const s = (sourceId: string, page: number, label?: string): SourceRef => ({ sourceId, page, label });


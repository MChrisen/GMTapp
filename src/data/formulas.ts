const formulaText = (input: FormulaInput) => {
  const variableNames = input.variables
    .map((variable) => `${variable.symbol} (${variable.name})`)
    .slice(0, 4)
    .join(', ');
  const extraVariables = input.variables.length > 4 ? ' m.fl.' : '';
  const firstAssumption = input.assumptions[0] ?? 'Kontrollér altid at forudsætningerne matcher opgaven.';
  return {
    description: `${input.name} hører til ${input.topic.toLowerCase()} og forbinder ${variableNames}${extraVariables}. Den bruges typisk som et hurtigt startpunkt, når nøgleordene i opgaven matcher ${input.keywords.slice(0, 4).join(', ')}.`,
    useWhen: categoryUseText[input.category],
    examTip: `${firstAssumption} Isolér den ukendte symbolsk først og indsæt tal med enheder til sidst.`,
  };
};

